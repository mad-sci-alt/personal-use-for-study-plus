public class forceConvert {
    public static void main(String[] args) {
        double b = 1.9999999999999999;
        int a = (int)b;
        System.out.println("Hello" + 10 + 3);
        System.out.println("Hello" + (10 + 3));
        System.out.println(b);
        System.out.println( a);
    }
}
