public class char01 {
    public static void main(String[] args) {
        char ch = 'a';
        System.out.println(ch);
        System.out.println((int)ch);

        char ch02 = 97;
        System.out.println(ch02 + 10);
        System.out.println((int)ch02);

        char ch03 = '\u0061';
        System.out.println(ch03 + 10);
        System.out.println((int)ch03);


    }
}
